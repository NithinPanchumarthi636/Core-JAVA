package core_examples;

public class Conditional_Statements {

	public static void main(String[] args) {

		int time = 23;
		if (time < 10) {
			System.out.println("Good Morning");
		} else if (time < 18) {
			System.out.println("Good Day");
		} else {
			System.out.println("Good Night");
		}

	}

}
